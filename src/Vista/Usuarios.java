    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int fila = jTable1.getSelectedRow();
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Debes seleccionar un usuario de la tabla.",
                "Sin selección", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Leer cada columna en su tipo correcto
        int id          = (int)    jTable1.getValueAt(fila, 0); // columna 0 = ID (int)
        String nombre   = (String) jTable1.getValueAt(fila, 1); // columna 1 = Nombre
        String apellido = (String) jTable1.getValueAt(fila, 2); // columna 2 = Apellido
        String telefono = (String) jTable1.getValueAt(fila, 3); // columna 3 = Telefono
        String correo   = (String) jTable1.getValueAt(fila, 4); // columna 4 = Correo
        String userName = (String) jTable1.getValueAt(fila, 5); // columna 5 = Username

        // El constructor es: (int id, String userName, String nombre, String apellido,
        //                      String telefono, String email, String contrasena)
        Usuario u = new Usuario(id, userName, nombre, apellido, telefono, correo, "");

        ActualizarUsuario form = new ActualizarUsuario();
        form.setFramePadre(this);
        form.setDatos(u);
        form.setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        int fila = jTable1.getSelectedRow();
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Debes seleccionar un usuario para eliminar.",
                "Sin selección", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirmar = javax.swing.JOptionPane.showConfirmDialog(this,
            "¿Estás seguro que deseas eliminar este usuario?",
            "Confirmar eliminación", javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirmar == javax.swing.JOptionPane.YES_OPTION) {
            int id = (int) jTable1.getValueAt(fila, 0);
            boolean exito = usuarioController.eliminar(id);
            if (exito) {
                javax.swing.JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente.");
                cargarTabla(); // Refresca la tabla automáticamente
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar el usuario.");
            }
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

