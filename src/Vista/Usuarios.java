    private void alternarColoresFilas() {
            jTable1.setDefaultRenderer(Object.class, new javax.swing.table.DefaultTableCellRenderer() {
            @Override
            public java.awt.Component getTableCellRendererComponent(
                    javax.swing.JTable table, Object value, boolean isSelected,
                    boolean hasFocus, int row, int column) {

                java.awt.Component c = super.getTableCellRendererComponent(
                        table, value, isSelected, hasFocus, row, column);

                // 🔵 Si está seleccionada
                if (isSelected) {
                    c.setBackground(new java.awt.Color(99, 121, 211));
                    c.setForeground(java.awt.Color.WHITE);
                }
                // 🟡 Si está en hover
                else if (row == filaHover) {
                    c.setBackground(new java.awt.Color(200, 220, 255)); // hover suave
                    c.setForeground(java.awt.Color.BLACK);
                }
                // ⚪ Filas normales alternadas
                else {
                    if (row % 2 == 0) {
                        c.setBackground(java.awt.Color.WHITE);
                    } else {
                        c.setBackground(new java.awt.Color(230, 240, 255));
                    }
                    c.setForeground(java.awt.Color.BLACK);
                }

                return c;
            }
        });
    }
    
    private void efectoHover() {
        jTable1.addMouseMotionListener(new java.awt.event.MouseMotionAdapter() {
            @Override
            public void mouseMoved(java.awt.event.MouseEvent e) {
                filaHover = jTable1.rowAtPoint(e.getPoint());
                jTable1.repaint();
            }
        });

        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseExited(java.awt.event.MouseEvent e) {
                filaHover = -1;
                jTable1.repaint();
            }
        });
    }
    
    

